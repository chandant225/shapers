        <h3>Mess Up Nights</h3>
        <p>The prevailing culture among Nepali professionals is to hide rather than accept our mistakes. The Shaping Mental Health team in Global Shapers Kathmandu Hub discovered through conversations with young Nepali professionals that they fear sharing their failures or ‘mess ups’ due to a fear of being judged, which over time has a direct impact on their well-being.</p>
        
       <p> We launched ‘Mess Up Nights’ to serve as a safe non-judgemental platform for young professionals to unburden themselves of their mess ups. The Hub also facilitates informal dialogue on areas related to well-being and mental health.</p>
        
        <p>Mess up Nights is a n person event that provides a platform for professionals to talk about their mess ups and own them. We had our first meeting on January 30, 2020 and a total of three mess up nights have been organized since.  mic.</p>
        

    <h3>Shaping Mental Health</h3>

    <p>Mental health is a growing and silent pandemic in Nepal. A recent national survey estimated that while 10% of adults had faced some mental health related problem, 94% of them did not seek help due to associated stigma. This was prior to COVID-19 related lockdown and restrictions, which have served to since accelerate the scale of mental health challenge in Nepal.</p> 
    
    <p>Poor awareness about mental health, its semantics and its spectrum in our community lie at the heart of the problem. They help constrain proper identification, labeling and communication of our emotions while also serving as a barrier to seeking help.</p>
    
    <p>Shapers from Kathmandu Hub, driven by individual mental health experiences and the desire to bring positive change in the existing status quo on mental health, launched a collective project titled Shaping Mental Health. The foundations for the project were built using learnings from another hub initiative (Building Emotional Resilience for Entrepreneurs) where young entrepreneurs were enabled to explore their emotional wellbeing through participatory.  The project now ties together multiple such individual initiatives all aimed towards our goal, to destigmatize mental health.</p>



        <p>The Entrepreneurs are expected to face and overcome challenges. They talk about their successes and maybe nowadays successful entrepreneurs even share their early failures. What is more often than not left out is the effect of starting an enterprise on entrepreneurs' emotional well being - the missed dinners, late work nights and anxiety over interest payments.</p>
        
